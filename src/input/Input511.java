package input;

/*
 * 문제
 * 두 개의 정수형 변수를 선언하고 값을 대입하여 아래와 같이 출력되는 프로그램을 작성하라.
 * 
 * 출력 예
 * 55 - 10 = 45
 * 2008 - 1999 = 9
 */

public class Input511 {

	public static void main(String [] args) {
		
		int iVar1 = 55 - 10;
		int iVar2 = 2008 - 1999;
		
		System.out.println("55 - 10 = " + iVar1);
		System.out.println("2008 - 1999 = " + iVar2);
		
	}//main
}//class